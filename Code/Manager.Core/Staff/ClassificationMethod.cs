Что не делать: Пытаться его удержать. Стоит помочь ему выплеснуть энергию."),
			new Class("Невзрачный",
@"Тип: невзрачный «средний» вид, абсолютная нейтральность

Что делать: Главным интересом будет постоянство, сохранение статуса-кво.

Что не делать: Пытаться вытащить его куда-то и поставить на видное место."),
		});

