Что не делать: «Вернуть его на землю» постоянной логики. Надо стараться найти тот особый смысл, который человек вкладывает в своё поведение. «Встать с ним за один прилавок.»"),
			new Class("Хипстер",
@"Тип: casual-внешность(«хипстер», «тусовщик»), быстрая речь(про отдых)

