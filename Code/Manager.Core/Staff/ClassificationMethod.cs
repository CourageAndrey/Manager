		public static readonly ClassificationMethod SevenRadicals = new ClassificationMethod("7 радикалов", new[]
		{
			new Class("Яркий",
@"Тип: «яркость» внешности и эгоцентричность(много «Я» в речи)

Что делать: Человек хорошо работает при наличии положительной обратной связи.

Что не делать: Игнорировать. Можно положительно его пиарить, можно отрицательно, но только не игнорировать."),
			new Class("Атлетичный",
@"Тип: атлетичность и диспластичность, косноязычие

